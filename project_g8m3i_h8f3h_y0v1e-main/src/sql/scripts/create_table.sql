eventType VARCHAR(30) PRIMARY KEY,
capacity INTEGER NOT NULL);

-- CREATE TABLE Event1 (
-- eventID INTEGER PRIMARY KEY,
-- startTime DATETIME NOT NULL,
-- endTime DATETIME NOT NULL,
